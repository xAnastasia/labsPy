#Первое и второе задание
seconds_per_min = 60
seconds_per_hour = seconds_per_min * seconds_per_min
print("Количество секунд в часе: " + str(seconds_per_hour))

#Третье задание
hour_per_day = 24
seconds_per_day = seconds_per_hour * hour_per_day
print("Количество секунд в сутках: " + str(seconds_per_day))

#Четвертое задание
result1 = seconds_per_day / seconds_per_hour
print("Результат 1: " + str(result1))

#Пятое задание
result2 = seconds_per_day // seconds_per_hour
print("Результат 2: " + str(result2))

